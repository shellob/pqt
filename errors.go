package pqt

import "errors"

// Маркерные ошибки публичного API. «Маркерная» — это значит, что сама
// ошибка ничего не делает, она нужна только для классификации: получив
// какую-нибудь ошибку из Validate, вызывающий код через errors.Is(err, X)
// определит, к какому виду она относится, и решит что с этим делать —
// HTTP-обработчик отдаст 401 или 403, логгер запишет нужный уровень,
// тест проверит, что упало именно то, что ожидали. Текст сообщения
// разбирать не нужно, все ошибки от Validate оборачиваются в одну из
// этих констант через fmt.Errorf("%w: ...", ErrXxx).
var (
	// ErrSignatureInvalid — подпись токена не сходится. Это значит, что
	// токен либо вообще не нашим ключом подписан, либо его байты были
	// изменены после подписи (в заголовке, в payload или в самой подписи).
	// В любом случае токену доверять нельзя — отвергаем.
	ErrSignatureInvalid = errors.New("pqt: signature is invalid")

	// ErrTokenExpired — срок действия токена истёк. Берётся claim exp
	// (время в Unix-секундах), сравнивается с текущим временем
	// (ValidateOptions.Clock); если now больше exp — ошибка. Если в
	// опциях задан Leeway, на эту величину допустима рассинхронизация
	// часов клиента и сервера. Сюда же попадает случай, когда exp
	// в токене отсутствует или равен нулю — по спецификации PQ-AT
	// бессрочные токены недопустимы.
	ErrTokenExpired = errors.New("pqt: token expired")

	// ErrIssuerMismatch — токен выпустил не тот, кого ждёт принимающая
	// сторона. claim iss в токене указывает на сервер-эмитента; если в
	// ValidateOptions.ExpectedIssuer задано конкретное ожидаемое значение
	// и оно не совпало — ошибка. Защита от ситуации, когда токен от чужого
	// сервиса (с тем же ключом или JWKS) примут как свой.
	ErrIssuerMismatch = errors.New("pqt: issuer mismatch")

	// ErrAudienceMismatch — токен выпущен не для нас. claim aud — это
	// идентификатор той системы, для которой токен предназначен; если
	// мы ждём свой aud, а в токене стоит чужой, ошибка. Защита от того,
	// что токен, выписанный для системы X, могут предъявить системе Y
	// и пройти у неё авторизацию.
	ErrAudienceMismatch = errors.New("pqt: audience mismatch")

	// ErrAlgMismatch — заявленный в токене алгоритм не совпадает с
	// алгоритмом ключа, который мы взяли для проверки подписи. Например,
	// в header.alg написано «mldsa65», а KeySource вернул ECDSA-ключ.
	//
	// Это защита от классической атаки на JWT-подобные форматы: если
	// сервер при проверке доверяет полю alg из заголовка, можно подменить
	// alg на более слабый или вообще на «none», и сервер примет токен.
	// Поэтому Validate смотрит на алгоритм ключа, который вернул KeySource,
	// и сверяет его с тем, что написано в токене. Любое расхождение —
	// отказ ещё ДО фактической проверки подписи.
	ErrAlgMismatch = errors.New("pqt: header alg does not match verifier algorithm")

	// ErrKeyNotFound — KeySource не нашёл подходящего ключа для проверки
	// этого токена. Например, в заголовке указан идентификатор ключа
	// (kid), а в наборе JWKS такого kid нет: либо токен старый и его
	// ключ давно удалён, либо токен подделан и kid не существует.
	ErrKeyNotFound = errors.New("pqt: verification key not found")

	// ErrInvalidOptions — параметры, переданные в Issue или Validate,
	// собраны неправильно. Например, в IssueOptions не указан Signer
	// (нечем подписывать), или указан неизвестный кодек/формат, или
	// в ValidateOptions нет KeySource (нечем выбрать ключ для проверки).
	// Это ошибка вызывающего кода, не ошибка токена.
	ErrInvalidOptions = errors.New("pqt: invalid options")

	// ErrTokenRevoked — токен числится в чёрном списке отозванных. После
	// успешной проверки подписи и срока действия Validate ещё раз
	// спрашивает у вызывающего: jti этого токена не отозван? Если
	// функция ValidateOptions.IsRevoked возвращает true — ошибка. Это
	// нужно, например, когда пользователь вышел из системы: токен сам
	// по себе ещё валиден (срок действия не истёк), но сервер должен
	// его игнорировать. Если IsRevoked в опциях не передан, проверка
	// пропускается.
	ErrTokenRevoked = errors.New("pqt: token revoked")
)
