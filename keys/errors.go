package keys

import "errors"

// Маркерные ошибки криптослоя. «Маркерная» — это значит, что сама ошибка
// ничего не делает; она нужна только для классификации, чтобы код выше
// через errors.Is(err, X) понял, какой именно вид сбоя случился, и отреагировал
// соответствующим образом. Текст сообщения добавляется к ошибке через
// fmt.Errorf("%w: ...", X) и предназначен для логов; разбирать его строкой
// не нужно — пользуйтесь errors.Is.
var (
	// ErrInvalidSignature — подпись не сошлась при проверке. Это значит,
	// что либо данные были подписаны не тем ключом, который мы используем
	// для проверки, либо после подписи кто-то изменил байты сообщения,
	// либо байты самой подписи не те. С точки зрения принимающей стороны
	// разница между этими тремя случаями значения не имеет: подпись
	// недействительна, токен надо отвергнуть.
	ErrInvalidSignature = errors.New("keys: invalid signature")

	// ErrInvalidKey — что-то не так с самим ключом. Например, нам отдали
	// байты публичного ключа, но в них не получилось разобрать ни одной
	// корректной точки на эллиптической кривой; или для ML-DSA байты
	// не складываются в валидный ключ нужного уровня. Возникает в основном
	// при разборе ключа из внешнего представления (JWK, raw-bytes).
	ErrInvalidKey = errors.New("keys: invalid key")

	// ErrAlgMismatch — пара ключей собирается из неподходящих частей.
	// Например, при сборке гибрида классическая половина должна быть
	// именно ECDSA P-256 — если туда передать ML-DSA, получится
	// ErrAlgMismatch. Или подпись с указанным alg ожидает ключ другого
	// типа, чем тот, что передан.
	ErrAlgMismatch = errors.New("keys: algorithm mismatch")

	// ErrUnsupportedAlg — указан алгоритм, которого мы не знаем (не
	// входит в список из alg.go) или который мы знаем, но не реализуем
	// в этом пакете.
	ErrUnsupportedAlg = errors.New("keys: unsupported algorithm")

	// ErrMalformedSignature — байты подписи структурно битые: разобрать
	// их в виде ожидаемой структуры не получается. Конкретные примеры:
	//
	//   - У гибрида подпись короче двух байт префикса длины — то есть
	//     там даже размер классической части не успели записать.
	//   - Поле «длина классической части» указывает за пределы всей
	//     подписи или ровно в её конец (т. е. для постквантовой части
	//     не остаётся места).
	//
	// От ErrInvalidSignature эта ошибка отличается тем, что туда мы
	// даже не дошли — байты не складываются в правильную форму, нечего
	// проверять криптографически.
	ErrMalformedSignature = errors.New("keys: malformed signature")
)
