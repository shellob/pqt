package keys

// PrivateKey — общий интерфейс для приватных (секретных) ключей. Любой
// тип, реализующий три метода ниже, считается приватным ключом и может
// использоваться для подписи.
//
// Через этот интерфейс верхний код общается с любой подписью — ECDSA,
// ML-DSA, гибрид — единообразно: получает байты сообщения, отдаёт
// байты подписи. Сам алгоритм спрятан внутри конкретной реализации.
//
// Реализации в этом пакете: ECDSAPrivateKey, MLDSAPrivateKey,
// HybridPrivateKey. Создаются конструкторами GenerateECDSA,
// GenerateMLDSA, NewHybrid, GenerateHybrid; они возвращают конкретный
// тип, а функции вроде pqt.Issue, которым ключ просто нужно передать,
// принимают этот интерфейс.
type PrivateKey interface {
	// Sign — подписать сообщение. На вход — произвольные байты (то, что
	// нужно подписать; в случае токена это склейка заголовка и payload).
	// На выход — байты подписи. Что именно в этих байтах, зависит от
	// конкретного алгоритма:
	//
	//   ecdsa-p256              — формат ASN.1 DER, около 70-72 байт.
	//                             ASN.1 DER — это стандартный бинарный
	//                             формат для записи структуры из двух
	//                             больших целых чисел (r, s), которые
	//                             и составляют ECDSA-подпись.
	//   mldsa44/65/87           — фиксированный размер из FIPS 204:
	//                             2420, 3309 и 4627 байт соответственно.
	//   hybrid-ecdsa-mldsa<n>   — сначала 2 байта с длиной классической
	//                             части (uint16, big-endian), потом сама
	//                             ECDSA-подпись, потом ML-DSA-подпись.
	//                             Префикс длины обязателен, потому что
	//                             ECDSA-подпись переменной длины (70 или
	//                             71 или 72 байта) — без длины разобрать,
	//                             где кончается ECDSA и начинается
	//                             ML-DSA, не получится.
	Sign(message []byte) (signature []byte, err error)

	// Public — отдать публичный ключ из этой пары. Используется, когда
	// есть приватный ключ, и нужно получить именно публичный — например,
	// чтобы опубликовать его в JWKS или передать в Validate.
	Public() PublicKey

	// Algorithm — какому алгоритму принадлежит ключ. Это значение
	// попадает в заголовок токена в поле alg.
	Algorithm() Alg
}

// PublicKey — общий интерфейс для публичных ключей. Они умеют только
// проверять подписи; подписывать ими нельзя (по самой природе
// асимметричной криптографии).
//
// Реализации парные к приватным: ECDSAPublicKey, MLDSAPublicKey,
// HybridPublicKey. Получаются обычно через метод Public() приватного
// ключа, или собираются из байтового представления (загруженного,
// например, из JWK).
type PublicKey interface {
	// Verify — проверить, что байты signature действительно являются
	// подписью байтов message, сделанной парным к этому публичному
	// приватным ключом.
	//
	// Возвращает:
	//   - nil, если подпись правильная;
	//   - ErrInvalidSignature, если подпись не сошлась — это самый
	//     частый отрицательный результат, означает что либо подписали
	//     не нашим ключом, либо байты сообщения после подписи изменились;
	//   - ошибку с тегом ErrMalformedSignature, если подпись структурно
	//     битая (для гибрида: длина классической части указывает на
	//     несуществующее место в буфере и т. п.) — то есть верифицировать
	//     даже не получилось начать;
	//   - ошибку с тегом ErrInvalidKey, если что-то не так с самим
	//     публичным ключом.
	Verify(message, signature []byte) error

	// Algorithm — какому алгоритму принадлежит ключ.
	Algorithm() Alg
}
