// Package keys — это криптографический слой PQ-AT.
//
// Здесь живут интерфейсы PrivateKey и PublicKey, а также три реализации:
// классическая ECDSA P-256, постквантовая ML-DSA (FIPS 204) уровней 44/65/87
// и гибрид «классическая подпись + постквантовая подпись» с параллельной
// проверкой обеих подписей сразу.
//
// Конкретные типы (ECDSAPrivateKey, MLDSAPrivateKey, HybridPrivateKey и
// соответствующие публичные ключи) сделаны экспортируемыми специально:
// зависимым пакетам, например jwk, нужен type switch по конкретному типу
// ключа. Сами конструкторы возвращают конкретные типы, а функции, которые
// берут ключ как параметр, принимают интерфейс — это стандартная идиома Go.
