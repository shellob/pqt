// Package keys — это криптографический слой PQ-AT, нижний этаж библиотеки.
//
// Здесь лежат две вещи: интерфейсы (PrivateKey и PublicKey), которыми
// наружу описывается «что умеет любой ключ», и три конкретные реализации
// этих интерфейсов:
//
//   - ECDSA P-256 — классическая подпись на эллиптической кривой. Тот же
//     алгоритм, что в JWT с alg=ES256. Подпись короткая (~70 байт),
//     проверяется быстро, но против квантового компьютера беззащитна.
//   - ML-DSA — постквантовая подпись по стандарту FIPS 204 (NIST PQC,
//     2024 год). Поддержаны три уровня стойкости: 44, 65, 87 (это
//     внутренние параметры алгоритма; чем больше, тем длиннее подпись
//     и медленнее работа, но выше запас стойкости). Подписи большие —
//     2-5 КБ, зато квантовому компьютеру их не сломать.
//   - Гибрид — комбинация двух предыдущих. Один и тот же текст
//     подписывается дважды, и токен признаётся валидным, только если обе
//     подписи в порядке. Так мы страхуемся: если сломают ECDSA (квантовый
//     компьютер) — спасёт ML-DSA; если в ML-DSA найдут уязвимость
//     (алгоритм молодой, теории за ним меньше) — спасёт ECDSA. Проверка
//     обеих подписей идёт параллельно через errgroup, чтобы не платить
//     за гибрид суммой двух отдельных проверок.
//
// Все три реализации скрывают свои внутренние детали и наружу выглядят
// одинаково — через интерфейсы PrivateKey/PublicKey. Любой код, который
// принимает «какой-нибудь ключ», работает с ними однообразно: вызывает
// Sign на приватном, Verify на публичном.
//
// # Почему конкретные типы экспортированы
//
// Помимо интерфейсов, экспортируются и сами структуры (ECDSAPrivateKey,
// MLDSAPrivateKey, HybridPrivateKey и парные публичные). Это сознательно:
// зависимые пакеты (например, jwk) при сериализации делают type switch —
// то есть смотрят, какой именно конкретный тип получился, и берут у него
// специфические для типа поля (X/Y у ECDSA, байты ключа у ML-DSA, две
// половины у гибрида). Без экспорта типов они бы не дотянулись до этих
// деталей.
//
// При этом конструкторы (GenerateECDSA, GenerateMLDSA, GenerateHybrid)
// всё равно возвращают конкретный тип, а функции, которым ключ просто
// нужно передать, принимают интерфейс PrivateKey/PublicKey. Это идиома
// стандартной библиотеки Go: «accept interfaces, return concrete types».
