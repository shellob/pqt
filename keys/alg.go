package keys

// Alg — это строковый идентификатор алгоритма подписи. То самое значение,
// которое попадает в заголовок токена в поле alg, и по которому
// проверяющая сторона понимает, чем токен подписан.
//
// Тип Alg сделан отдельным (не просто string), чтобы у него были методы
// (Valid, IsHybrid) и чтобы его нельзя было случайно перепутать с любой
// другой строкой при передаче в функции.
type Alg string

// Идентификаторы алгоритмов из спецификации PQ-AT (раздел 2.2 диссертации).
// Эти конкретные строки попадают в поле alg заголовка выпускаемых токенов.
const (
	// AlgECDSAP256 — классическая ECDSA на эллиптической кривой P-256
	// (то же, что в JWT с alg=ES256). Это привычная всем подпись
	// длиной около 70 байт. Нужна в двух ролях: для совместимости со
	// старыми сервисами, которые умеют только классические алгоритмы,
	// и как «классическая половина» гибридных режимов ниже.
	AlgECDSAP256 Alg = "ecdsa-p256"

	// AlgMLDSA44 — постквантовая ML-DSA уровня 2 по стандарту FIPS 204.
	// «Уровень 2» здесь значит: NIST оценивает её устойчивость к атакам
	// сравнимой с AES-128 — самый низкий из принятых уровней. Подпись
	// 2420 байт, открытый ключ 1312 байт.
	AlgMLDSA44 Alg = "mldsa44"

	// AlgMLDSA65 — ML-DSA уровня 3 (≈ AES-192). Подпись 3309 байт,
	// открытый ключ 1952 байт. Это **целевой** уровень для PQ-AT:
	// разумный баланс между запасом стойкости и размером.
	AlgMLDSA65 Alg = "mldsa65"

	// AlgMLDSA87 — ML-DSA уровня 5 (≈ AES-256), самый «тяжёлый» из
	// доступных. Подпись 4627 байт, открытый ключ 2592 байт. Используется,
	// когда нужен максимальный запас стойкости и размер не критичен.
	AlgMLDSA87 Alg = "mldsa87"

	// AlgHybridECDSAMLDSA44 — гибрид ECDSA P-256 и ML-DSA уровня 2.
	// Над одним и тем же сообщением ставятся обе подписи; токен валиден
	// только если обе сверились. Подробнее про устройство гибрида —
	// см. файл hybrid.go.
	AlgHybridECDSAMLDSA44 Alg = "hybrid-ecdsa-mldsa44"

	// AlgHybridECDSAMLDSA65 — гибрид ECDSA P-256 и ML-DSA уровня 3.
	// **Целевой** гибридный режим для PQ-AT: дешёвая классическая
	// подпись для скорости + постквантовая mldsa65 для запаса.
	AlgHybridECDSAMLDSA65 Alg = "hybrid-ecdsa-mldsa65"

	// AlgHybridECDSAMLDSA87 — гибрид ECDSA P-256 и ML-DSA уровня 5.
	AlgHybridECDSAMLDSA87 Alg = "hybrid-ecdsa-mldsa87"
)

// String возвращает строковое значение алгоритма (на случай, когда нужно
// положить его, например, в JSON или в лог). Удовлетворяет интерфейсу
// fmt.Stringer.
func (a Alg) String() string { return string(a) }

// IsHybrid говорит, относится ли данный алгоритм к гибридным (классика +
// постквантовая) или это «одиночный» алгоритм. Полезно в коде, который
// специально работает с гибридом — например, при сборке гибридного
// ключа из двух обычных.
func (a Alg) IsHybrid() bool {
	switch a {
	case AlgHybridECDSAMLDSA44, AlgHybridECDSAMLDSA65, AlgHybridECDSAMLDSA87:
		return true
	default:
		return false
	}
}

// Valid отвечает на вопрос «является ли строка одним из известных нам
// идентификаторов алгоритмов». Используется при разборе входящих токенов:
// если в заголовке указан незнакомый alg, токен надо отвергнуть до
// любых дальнейших операций — значит либо токен от другой системы,
// либо это попытка подмены, либо мы просто не понимаем что нам прислали.
func (a Alg) Valid() bool {
	switch a {
	case AlgECDSAP256,
		AlgMLDSA44, AlgMLDSA65, AlgMLDSA87,
		AlgHybridECDSAMLDSA44, AlgHybridECDSAMLDSA65, AlgHybridECDSAMLDSA87:
		return true
	default:
		return false
	}
}
