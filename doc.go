// Package pqt — корневой пакет проекта PQ-AT (Post-Quantum Access Token).
//
// Реализация формата постквантового токена доступа с гибридной схемой
// подписи (ECDSA P-256 + ML-DSA-65) и компактным бинарным кодированием.
// Спецификация формата изложена в магистерской диссертации
// «Построение токена постквантового доступа» (Тимофеенко С. В.,
// ВГУ ФКН, 2026), глава 2. Реализация — глава 3, эксперимент — глава 4.
//
// Поддерживаются три режима подписи:
//   - ecdsa-p256          — классическая ECDSA на кривой P-256;
//   - mldsa44/65/87       — постквантовая ML-DSA трёх уровней (FIPS 204);
//   - hybrid-ecdsa-mldsa<n> — гибрид: обе подписи над одним сообщением.
//
// Полезная нагрузка кодируется в JSON или CBOR (CWT-стиль, RFC 8392),
// токен сериализуется текстовым форматом, совместимым с JWT (RFC 7519),
// либо компактным бинарным форматом с длиной-префиксом.
//
// # Подпакеты
//
//   - keys — криптографический слой: интерфейсы PrivateKey/PublicKey,
//     реализации ECDSA, ML-DSA и универсальный гибрид;
//   - jwk  — сериализация ключей в формат JSON Web Key.
//
// На последующих этапах добавятся подпакеты для формата токена, кодеков,
// сериализаторов и серверов авторизации/ресурсов.
