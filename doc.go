// Package pqt — корневой пакет проекта PQ-AT (Post-Quantum Access Token,
// постквантовый токен доступа).
//
// PQ-AT — это формат токена, по структуре повторяющий JWT (заголовок,
// полезная нагрузка, подпись), но с двумя отличиями: используются
// постквантовые алгоритмы подписи (одни или в паре с классическими),
// и поддерживается компактное бинарное кодирование. Это нужно, потому
// что у постквантовых подписей размер исчисляется килобайтами, и без
// сжатия токен перестанет помещаться в HTTP-заголовок.
//
// Формат описан в магистерской диссертации «Построение токена
// постквантового доступа» (Тимофеенко С. В., ВГУ ФКН, 2026), глава 2.
// Реализация — глава 3, эксперимент — глава 4.
//
// # Режимы подписи
//
// В заголовке токена в поле alg указывается один из трёх режимов:
//
//   - ecdsa-p256 — классическая подпись ECDSA на эллиптической кривой
//     P-256. Привычный JWT-режим, нужен для совместимости со старыми
//     системами и как классическая половина гибрида.
//   - mldsa44, mldsa65, mldsa87 — постквантовая подпись ML-DSA по
//     стандарту FIPS 204. Цифры 44/65/87 — три уровня стойкости (NIST
//     уровни 2/3/5). Целевой уровень для PQ-AT — mldsa65: разумный
//     баланс между стойкостью и размером подписи (3309 байт).
//   - hybrid-ecdsa-mldsa44/65/87 — гибрид: над одним и тем же сообщением
//     ставится сразу две подписи (ECDSA и ML-DSA), и токен считается
//     валидным только когда обе проверки прошли. Это страховка на
//     переходный период: если сломают одну схему, останется другая.
//
// # Кодирование полезной нагрузки и форматы токена
//
// Утверждения (claims) — sub, iss, aud, exp и т. д., как в JWT —
// кодируются либо в JSON со строковыми именами полей (RFC 7519), либо
// в CBOR с целочисленными ключами в стиле CWT (RFC 8392). CBOR
// компактнее на 2-20% в зависимости от того, сколько в токене claims.
//
// Готовый токен можно собрать в одном из двух видов:
//
//   - Текстовый, совместимый с JWT: три части, разделённые точками,
//     каждая закодирована в base64url. Подходит для HTTP-заголовка
//     Authorization: Bearer и проходит через любые прокси.
//   - Бинарный, компактный: подряд лежат длина заголовка (2 байта),
//     сам заголовок, длина payload (2 байта), сам payload и подпись.
//     Без накладных расходов base64url, на 25% меньше по размеру.
//
// # Подпакеты
//
// Библиотека разбита на слои; каждый занимается своей задачей и не
// знает о слоях выше:
//
//   - keys — криптографические примитивы: интерфейсы PrivateKey/PublicKey,
//     реализации ECDSA, ML-DSA и гибрид. Ничего не знает о токенах.
//   - jwk — сохранение ключей в JSON-файлы и обратное чтение в формате
//     JSON Web Key (RFC 7517).
//   - token — формат токена: тип Header, тип Claims, кодеки и
//     сериализаторы. Сам ничего не подписывает — на вход и выход
//     получает уже готовые байты подписи.
//
// Сам пакет pqt экспортирует публичный API библиотеки — функции Issue,
// Parse и Validate, которые склеивают криптослой и формат в обычный
// сценарий использования: «выдай токен» и «проверь токен».
package pqt
