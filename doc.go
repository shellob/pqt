// Package pqt — корневой пакет проекта PQ-AT (Post-Quantum Access Token).
//
// Здесь реализован формат постквантового токена доступа: гибридная подпись
// (ECDSA P-256 + ML-DSA-65) и компактное бинарное кодирование. Сама
// спецификация описана в магистерской диссертации «Построение токена
// постквантового доступа» (Тимофеенко С. В., ВГУ ФКН, 2026), глава 2.
// Реализация — глава 3, эксперимент — глава 4.
//
// Поддерживаются три режима подписи:
//   - ecdsa-p256            — классическая ECDSA на кривой P-256.
//   - mldsa44/65/87         — постквантовая ML-DSA трёх уровней (FIPS 204).
//   - hybrid-ecdsa-mldsa<n> — гибрид, где над одним и тем же сообщением
//     ставится сразу две подписи: классическая и постквантовая.
//
// Полезная нагрузка кодируется в JSON или в CBOR (стиль CWT, RFC 8392).
// Готовый токен сериализуется в один из двух форматов: текст, совместимый
// с JWT (RFC 7519), или компактный бинарный поток с длиной-префиксом.
//
// # Подпакеты
//
//   - keys  — криптографический слой: интерфейсы PrivateKey/PublicKey,
//     реализации ECDSA, ML-DSA и гибридная схема.
//   - jwk   — сериализация ключей в формат JSON Web Key.
//   - token — формат токена: Header, Claims, кодеки JSON/CBOR, текстовый
//     и бинарный варианты сериализации.
//
// На следующих этапах появятся подпакеты для выпуска и проверки токенов
// (Issuer/Validator) и для серверов авторизации и ресурсов.
