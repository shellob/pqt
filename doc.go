// Package pqt реализует формат постквантового токена доступа PQ-AT
// с гибридной схемой подписи (ECDSA P-256 + ML-DSA-65) и компактным
// бинарным кодированием.
//
// Спецификация формата изложена в магистерской диссертации
// «Построение токена постквантового доступа» (Тимофеенко С. В., ВГУ ФКН, 2026),
// глава 2. Реализация — глава 3, эксперимент — глава 4.
//
// Поддерживаются три режима подписи:
//   - ecdsa-p256          — классическая ECDSA на кривой P-256;
//   - mldsa65             — постквантовая ML-DSA уровня 3 (FIPS 204);
//   - hybrid-ecdsa-mldsa65 — гибрид: обе подписи над одним и тем же сообщением.
//
// Полезная нагрузка кодируется в JSON или CBOR (CWT-стиль, RFC 8392),
// токен сериализуется текстовым форматом, совместимым с JWT (RFC 7519),
// либо компактным бинарным форматом с длиной-префиксом.
