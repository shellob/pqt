package token

// Format — это способ склеить три части токена (заголовок, payload и
// подпись) в один поток байт для передачи. На само содержимое частей
// он не влияет — только на способ их упаковки.
//
// В отличие от Codec, значение Format в самом токене не записывается:
// формат определяется по контексту, в котором токен передаётся. В
// HTTP-заголовке Authorization: Bearer всегда text (там по сути любая
// бинарь не пройдёт); в gRPC-метадате и других бинарных каналах —
// binary. Принимающая сторона знает контекст и сама указывает формат
// при разборе.
type Format string

const (
	// FormatText — JWT-совместимый текстовый вид:
	//
	//   Base64url(H).Base64url(P).Base64url(Sig)
	//
	// Каждая часть отдельно кодируется в base64url (это base64 с
	// заменой '+' и '/' на '-' и '_', без хвостовых '='), потом три
	// получившихся блока соединяются через точку. Этот формат можно
	// класть в HTTP-заголовок Authorization: Bearer, и он пройдёт
	// через любые прокси, которые ожидают только ASCII.
	FormatText Format = "text"

	// FormatBinary — компактный бинарный вид:
	//
	//   [2 байта длины H, big-endian] [байты H] [2 байта длины P] [байты P] [подпись]
	//
	// Никакого base64, длины частей записаны явно как uint16 в
	// big-endian. Размер на 25% меньше, чем у текстового, потому что
	// base64url раздувает данные ровно на эти 33% (4/3 ≈ 1.33).
	// Используется в каналах, которые могут передавать произвольные
	// байты — gRPC, WebSocket, собственные бинарные протоколы.
	FormatBinary Format = "binary"
)

// String — текстовое имя формата (то же значение, что и внутренняя
// строка). Удовлетворяет интерфейсу fmt.Stringer.
func (f Format) String() string { return string(f) }

// Valid отвечает на вопрос «эта строка — один из известных нам форматов?».
// Используется при разборе ValidateOptions/IssueOptions, чтобы рано
// отвергнуть неправильно собранные опции.
func (f Format) Valid() bool {
	switch f {
	case FormatText, FormatBinary:
		return true
	default:
		return false
	}
}
