// Package token реализует формат токена PQ-AT — то есть «как именно
// данные раскладываются в байты». Криптографических подписей этот пакет
// сам не делает, он только описывает структуру токена и умеет её
// собирать и разбирать.
//
// Полное описание формата — в разделе 2.2 диссертации; ниже краткий
// разбор, чтобы пакет можно было читать без обращения к тексту.
//
// # Из чего состоит токен
//
// PQ-AT, как и JWT, состоит из трёх частей:
//
//  1. Заголовок (Header) — служебная информация о самом токене:
//     алгоритм подписи, версия формата, тип, способ кодирования
//     полезной нагрузки, идентификатор ключа. Заголовок всегда
//     сериализуется в JSON со строковыми именами полей. CBOR для
//     заголовка не используется специально: разбирающий код должен
//     успеть прочитать его до того, как поймёт, в каком формате
//     payload — а значит формат заголовка должен быть фиксирован.
//  2. Полезная нагрузка (Payload) — набор claims, утверждений о
//     пользователе: sub (subject), iss (issuer), aud (audience),
//     exp (expiry), iat (issued at), jti (JWT ID), scope, kind.
//     Эти имена — стандартные для JWT (RFC 7519). Payload можно
//     кодировать либо в JSON, либо в CBOR; способ задаётся полем
//     enc в заголовке.
//  3. Подпись (Signature) — байты, которые получаются, когда некий
//     приватный ключ подписывает склейку байтов заголовка и payload.
//     Сам этот пакет подпись не считает; она приходит на вход
//     SerializeText/SerializeBinary как готовый параметр и кладётся
//     в третью часть токена.
//
// # Два кодека и два формата
//
// Кодек определяет, как сериализуется payload — JSON или CBOR.
// CBOR (RFC 8949) — это бинарная альтернатива JSON; за счёт того, что
// поля идут с целочисленными ключами в стиле CWT (RFC 8392, 1=sub,
// 2=iss, ...) и без кавычек/двоеточий, payload получается на 2-20%
// компактнее, чем в JSON.
//
// Формат токена — это способ склеить три части (header, payload,
// signature) в один поток байт:
//
//   - Текстовый, совместимый с JWT:
//
//     Base64url(H).Base64url(P).Base64url(Sig)
//
//     Каждая часть отдельно кодируется в base64url (это base64 с
//     заменой '+' и '/' на '-' и '_', без хвостовых '='), потом
//     результаты склеиваются через точку. В таком виде токен можно
//     класть в HTTP-заголовок Authorization: Bearer и пропускать
//     через любые прокси, которые ожидают ASCII-символы.
//
//   - Бинарный, компактный:
//
//     [2 байта длины H, big-endian] [байты H] [2 байта длины P] [байты P] [подпись]
//
//     Никакого base64, длины частей записаны явно. На 25% меньше
//     по размеру, чем текстовый, потому что base64url раздувает
//     данные ровно настолько (4/3 ≈ 1.33).
//
// # Что делает пакет, а что нет
//
// Пакет умеет: кодировать и декодировать заголовок и payload, склеивать
// три части в текст или бинарь, разбирать готовый токен обратно. Не
// умеет: считать подпись, проверять подпись, генерировать ключи —
// этим занимаются пакет keys (криптография) и корневой pqt (склейка
// в реальный сценарий выпуска и проверки).
