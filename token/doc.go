// Package token реализует формат токена PQ-AT, описанный в разделе 2.2
// диссертации.
//
// Токен состоит из трёх частей: заголовок, payload и подпись. Заголовок всегда
// сериализуется в JSON и содержит метаинформацию: алгоритм подписи, версию
// формата, тип, способ кодирования payload и идентификатор ключа. Payload —
// это набор JWT-совместимых claims; кодируется либо в JSON, либо в CBOR
// с целочисленными ключами в стиле CWT (RFC 8392).
//
// Готовый токен можно собрать в один из двух форматов:
//
//   - Текстовый, совместимый с JWT: Base64url(H).Base64url(P).Base64url(Sig).
//     Подходит для HTTP-заголовка Authorization: Bearer и обычных прокси.
//   - Бинарный, компактный: [2 байта длины H] H [2 байта длины P] P [подпись].
//     Без накладных расходов Base64url (примерно 33%), нужен в gRPC/WebSocket.
//
// Подпись считается над склейкой сериализованных заголовка и payload в том же
// порядке, в котором они кладутся в токен. Сам пакет ничего не подписывает —
// этим занимаются функции Issue и Validate из корневого пакета pqt; они
// используют криптослой keys.
