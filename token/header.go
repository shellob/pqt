package token

import (
	"bytes"
	"encoding/json"
	"fmt"

	"pqt/keys"
)

// TypePQAT — это значение, которое обязательно должно лежать в поле typ
// заголовка любого токена PQ-AT. По нему получатель отличает «свои»
// токены от посторонних форматов вроде JWT (там в typ обычно "JWT").
// Несовпадение — повод сразу отвергнуть токен.
const TypePQAT = "PQ-AT"

// CurrentVersion — номер версии формата токена, который понимает эта
// сборка библиотеки. Заголовок с Ver вне диапазона [1, CurrentVersion]
// не принимается. То есть мы не делаем вид, что разобрали токен из
// будущей, ещё неизвестной нам версии формата — а честно говорим
// «не понимаю». Это страховка от ситуации, когда в новой версии формата
// поменяется семантика какого-то поля, а наш старый код, не зная об
// этом, проверит токен по-старому и пропустит то, что не должно было
// проходить.
const CurrentVersion = 1

// Header — заголовок токена PQ-AT, описанный в разделе 2.2 диссертации.
//
// Заголовок всегда сериализуется в JSON со строковыми именами полей —
// независимо от того, как закодирован payload. Это сознательный выбор:
// при разборе токена сначала нужно понять, в каком кодеке лежит payload
// (это говорит само поле enc заголовка), а значит формат заголовка
// должен быть единственным, чтобы не получился цикл «чтобы понять
// формат, надо сначала прочитать формат».
//
// # Поля
//
//   - Alg — идентификатор алгоритма подписи (см. keys.Alg).
//   - Ver — версия формата (целое число от 1 до CurrentVersion).
//   - Typ — тип токена, всегда "PQ-AT".
//   - Enc — кодек payload, json или cbor. Не путать с одноимённым
//     полем в JWE (RFC 7516), где enc означает encryption (шифрование).
//     У нас enc — это encoding, способ кодирования; шифрования в PQ-AT
//     нет, токен везде в открытом виде.
//   - Kid — идентификатор ключа (key ID). Опциональное поле; нужно,
//     когда у эмитента в обороте несколько ключей одновременно (плановая
//     ротация). Получатель токена по Kid выберет в JWKS правильный
//     публичный ключ для проверки.
//
// Множество полей заголовка строго фиксировано: декодер отвергает
// токен, если в JSON встречается какое-то поле, которого нет в этой
// структуре. Без такого ограничения можно было бы протащить в подписанный
// заголовок «лишний» параметр со скрытой семантикой; жёсткий список
// заставляет любое расширение формата явно поднять CurrentVersion и
// быть совместимым с предыдущими сборками.
type Header struct {
	Alg keys.Alg `json:"alg"`
	Ver int      `json:"ver"`
	Typ string   `json:"typ"`
	Enc Codec    `json:"enc"`
	Kid string   `json:"kid,omitempty"`
}

// NewHeader создаёт заголовок с разумными значениями по умолчанию:
// Typ = "PQ-AT" и Ver = CurrentVersion. От вызывающего нужны только
// те поля, которые действительно меняются от вызова к вызову: алгоритм,
// кодек payload и (опционально) идентификатор ключа.
func NewHeader(alg keys.Alg, enc Codec, kid string) Header {
	return Header{
		Alg: alg,
		Ver: CurrentVersion,
		Typ: TypePQAT,
		Enc: enc,
		Kid: kid,
	}
}

// EncodeHeader сериализует заголовок в JSON-байты. Перед записью
// проверяется, что значения полей осмысленны (алгоритм и кодек —
// известные, версия в диапазоне, typ совпадает с TypePQAT). Так мы
// никогда не выпустим токен с заведомо битым заголовком.
func EncodeHeader(h Header) ([]byte, error) {
	if err := h.validate(); err != nil {
		return nil, err
	}
	b, err := json.Marshal(h)
	if err != nil {
		return nil, fmt.Errorf("token: encode header: %w", err)
	}
	return b, nil
}

// DecodeHeader разбирает заголовок из JSON-байт и проверяет его поля.
//
// Декодер строгий по двум направлениям:
//
//  1. DisallowUnknownFields — любое поле, которого нет в структуре
//     Header, приведёт к ошибке. Это и есть фиксация «закрытого
//     множества» полей, описанная выше у Header.
//  2. dec.More() после Decode — проверка, что после закрывающей
//     фигурной скобки заголовка не осталось ничего больше. Без этой
//     проверки злоумышленник мог бы протащить в подписанные байты
//     второй заголовок: формально json.Decode остановился бы на первом
//     валидном объекте и игнорировал хвост, а проверяющая сторона
//     при разборе могла бы случайно «выбрать» этот хвост вместо
//     первого заголовка.
//
// Возвращаемые ошибки:
//
//   - ErrMalformed — JSON битый, есть лишние поля или мусор после
//     объекта (то есть проблема в самой структуре байт).
//   - ErrInvalidHeader — JSON валидный, но значения полей не подходят:
//     неизвестный alg, неизвестный enc, версия вне диапазона, не тот typ.
func DecodeHeader(data []byte) (Header, error) {
	dec := json.NewDecoder(bytes.NewReader(data))
	dec.DisallowUnknownFields()

	var h Header
	if err := dec.Decode(&h); err != nil {
		return Header{}, fmt.Errorf("%w: разбор JSON заголовка: %w", ErrMalformed, err)
	}
	if dec.More() {
		return Header{}, fmt.Errorf("%w: после объекта заголовка остались лишние данные", ErrMalformed)
	}
	if err := h.validate(); err != nil {
		return Header{}, err
	}
	return h, nil
}

// validate проверяет, что значения полей заголовка осмысленны. Все
// четыре поля обязательны и должны быть из определённого набора —
// иначе мы либо не сможем дальше работать с этим токеном (неизвестен
// алгоритм или кодек), либо будем работать неправильно (не та версия
// формата, не тот тип).
func (h Header) validate() error {
	if !h.Alg.Valid() {
		return fmt.Errorf("%w: неизвестный alg %q", ErrInvalidHeader, h.Alg)
	}
	if !h.Enc.Valid() {
		return fmt.Errorf("%w: неизвестный enc %q", ErrInvalidHeader, h.Enc)
	}
	if h.Ver < 1 || h.Ver > CurrentVersion {
		return fmt.Errorf("%w: ver должен быть в диапазоне [1, %d], получено %d",
			ErrInvalidHeader, CurrentVersion, h.Ver)
	}
	if h.Typ != TypePQAT {
		return fmt.Errorf("%w: typ должен быть %q, получено %q", ErrInvalidHeader, TypePQAT, h.Typ)
	}
	return nil
}
