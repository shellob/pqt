package token

import "errors"

// Маркерные ошибки пакета. Используются вместе с errors.Is, чтобы код
// выше по стеку (например, Validate из корневого пакета pqt или
// HTTP-обработчики в авторизационном сервере) мог отличать «токен
// структурно битый» от «значения полей не подходят» без разбора
// текста сообщений.
var (
	// ErrMalformed — байты токена не складываются в правильную форму.
	// Это про структуру, а не про смысл. Возникает, например, когда:
	//   - в текстовом формате частей оказалось не три (или какая-то
	//     из них не валидный base64url);
	//   - в бинарном формате не хватает байтов на префикс длины или
	//     заявленная в префиксе длина выходит за границы;
	//   - JSON в заголовке битый или после закрывающей скобки идёт
	//     что-то ещё;
	//   - в CBOR-payload встречается дубликат ключа в map (см.
	//     DecodePayload).
	ErrMalformed = errors.New("token: malformed")

	// ErrInvalidHeader — заголовок прочитался без проблем (структура
	// в порядке), но значения полей нам не подходят. Возникает, когда
	// в заголовке указан незнакомый нам alg или enc, или версия формата
	// (Ver) вне диапазона [1, CurrentVersion], или поле typ не равно
	// "PQ-AT". Это не значит, что заголовок битый — он просто описывает
	// токен, который мы понять не в силах.
	ErrInvalidHeader = errors.New("token: invalid header")

	// ErrUnsupportedCodec — попытались сериализовать или разобрать
	// payload через кодек, которого мы не знаем (что-то отличное от
	// json и cbor). Эта ошибка отдельная от ErrInvalidHeader, потому
	// что Codec может быть передан и без заголовка — в EncodePayload
	// он приходит параметром.
	ErrUnsupportedCodec = errors.New("token: unsupported codec")
)
