package token

// Codec — это идентификатор способа сериализации payload токена. Само
// значение записывается в поле Header.Enc; на стороне выпуска оно
// говорит «закодируй payload вот так», а на стороне проверки —
// «разбирай payload именно этим способом, потому что вот так его
// и закодировали».
type Codec string

const (
	// CodecJSON — обычный JSON (RFC 8259) со строковыми именами полей
	// ("sub", "iss" и т. д.). Тот же кодек, который используется в JWT.
	// Удобен тем, что payload можно прочитать глазами при отладке —
	// например, через CLI-команду `pqt-cli decode` или через любой
	// JSON-вьюер. Цена удобства — размер: каждое строковое имя плюс
	// кавычки и двоеточия добавляют байтов.
	CodecJSON Codec = "json"

	// CodecCBOR — бинарный CBOR (Concise Binary Object Representation,
	// RFC 8949) с целочисленными ключами в стиле CWT (CBOR Web Token,
	// RFC 8392). Вместо строки "sub" в CBOR пишется целое число 1,
	// вместо "iss" — 2 и т. д.; разделители полей не нужны, потому что
	// CBOR сам кодирует структуру в байтах. На типовом наборе из 7-8
	// claims это даёт примерно 2-20% выигрыша по размеру по сравнению
	// с JSON (точная цифра зависит от длины самих значений: чем длиннее
	// строки в payload, тем меньше относительный выигрыш).
	//
	// Глазами CBOR не читается — нужен hex-дамп или специальный декодер.
	CodecCBOR Codec = "cbor"
)

// String — текстовое представление кодека (то же значение, что и
// внутренняя строка). Удовлетворяет интерфейсу fmt.Stringer.
func (c Codec) String() string { return string(c) }

// Valid отвечает на вопрос «эта строка — один из известных нам кодеков?».
// Используется при разборе входящих токенов: если в Header.Enc стоит
// что-то незнакомое, токен надо отвергнуть, не пытаясь декодировать
// payload.
func (c Codec) Valid() bool {
	switch c {
	case CodecJSON, CodecCBOR:
		return true
	default:
		return false
	}
}
