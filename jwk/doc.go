//
// Поддерживаемые типы ключей (поле "kty"):
//   - "EC"     — ECDSA P-256 (RFC 7517 / RFC 7518 §6.2);
//   - "MLDSA"  — ML-DSA уровней 44 / 65 / 87 (расширение PQ-AT);
//   - "Hybrid" — гибридная пара ECDSA + ML-DSA (расширение PQ-AT).
//
// Расширения "MLDSA" и "Hybrid" не входят в IANA-реестр JWK, но описаны в
// разделе 2 диссертации и согласованы по структуре с CWT (RFC 8392) и
// традиционным JWK для EC-ключей.
