//   - "Hybrid" — гибридная пара ECDSA + ML-DSA. Тоже наше расширение.
//
// «MLDSA» и «Hybrid» в IANA-реестре JWK не зарегистрированы, но описаны в
// разделе 2 диссертации и по структуре согласованы с обычным JWK для EC и
// с CWT (RFC 8392).
