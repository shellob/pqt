// Package jwk превращает криптографические ключи в JSON-файлы и
// обратно. Формат — JSON Web Key (RFC 7517): стандартный способ
// записывать публичные и приватные ключи в виде небольшого JSON-объекта,
// чтобы их можно было хранить, передавать через HTTP и разбирать
// единообразно.
//
// Зачем это нужно. Криптографический ключ внутри программы — это
// специальный Go-объект с математическими полями. Сохранить его в
// файл, прислать другому сервису или показать пользователю в таком
// виде нельзя. JWK даёт простой текстовый эквивалент: все поля ключа
// раскладываются по строкам JSON, бинарные значения превращаются в
// base64url. Эта же библиотека читает JWK обратно — и в памяти снова
// получается живой Go-объект ключа.
//
// # Какие типы ключей поддерживаются
//
// Поле "kty" в JWK указывает тип ключа. У нас три значения:
//
//   - "EC" — классические ECDSA-ключи на эллиптической кривой P-256.
//     Это стандарт, как в RFC 7517 / RFC 7518 §6.2: поля x, y для
//     публичной части и d для приватной (всё в base64url).
//   - "MLDSA" — постквантовые ML-DSA-ключи уровней 44, 65 или 87.
//     В IANA-реестре JWK таких типов пока нет; мы добавляем своё:
//     поля pub (публичный ключ) и priv (приватный) в base64url, плюс
//     обычное поле alg, по которому видно конкретный уровень.
//   - "Hybrid" — пара из классической и постквантовой половин.
//     Тоже наше расширение. Хранится как массив components из двух
//     обычных JWK: первый — классическая половина, второй — постквантовая.
//
// Расширения "MLDSA" и "Hybrid" не зарегистрированы в IANA, но описаны
// в разделе 2 диссертации и по структуре согласованы с обычным JWK
// для EC-ключей и с CWT (CBOR Web Token, RFC 8392). То есть для
// сторонних систем они выглядят как валидный JSON, просто с непривычным
// kty — это и есть нормальный путь расширения формата.
