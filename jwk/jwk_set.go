package jwk

import "encoding/json"

// Set — это набор JWK, как описано в RFC 7517 §5 (там его называют
// «JWK Set», или JWKS). Используется тогда, когда нужно опубликовать
// сразу несколько ключей — например, на эндпоинте сервера авторизации
// /.well-known/pq-jwks.
//
// Зачем нужен сразу набор. На сервере в обороте может быть несколько
// ключей: например, во время плановой смены ключей старый ещё
// действует (потому что в обращении остались токены, подписанные им),
// и одновременно используется новый. Клиенты-валидаторы скачивают
// весь набор; в каждом токене в заголовке указан kid (идентификатор
// ключа), и валидатор по этому kid находит в наборе нужную запись.
//
// Структура простая — всего одно поле Keys. Имя именно такое, потому
// что этого требует RFC 7517 §5.1: внешний JSON-объект должен
// называться "keys".
type Set struct {
	Keys []JWK `json:"keys"`
}

// MarshalJSON — кастомная сериализация. Без неё пустой набор (когда
// поле Keys равно nil) превратился бы в JSON `{"keys":null}`, что
// формально допустимо, но на практике может ломать клиентов на других
// языках: некоторые JWKS-парсеры (например, на Python или Java)
// строго требуют, чтобы поле keys было массивом — пусть и пустым, —
// и на null упадут с ошибкой.
//
// Поэтому при сериализации мы подставляем пустой слайс вместо nil:
// получается `{"keys":[]}`, и любой стандартный клиент с этим работает.
func (s Set) MarshalJSON() ([]byte, error) {
	keys := s.Keys
	if keys == nil {
		keys = []JWK{}
	}
	type wire struct {
		Keys []JWK `json:"keys"`
	}
	return json.Marshal(wire{Keys: keys})
}

// Find ищет в наборе ключ с указанным kid. Возвращает найденный JWK
// и true; если ключа с таким kid нет — пустой JWK и false.
//
// Перебор простой и линейный: набор JWKS обычно содержит единицы,
// максимум десятки ключей, так что строить хеш-индекс смысла нет.
//
// Если в наборе оказались два ключа с одинаковым kid (RFC 7517 §4.5
// рекомендует уникальность, но не требует — и при ротации иногда
// возникают такие пересечения), Find вернёт первый встречный. Это
// детерминированно: при одном и том же входном Set всегда тот же
// результат.
//
// Пустой kid в запросе обрабатывается отдельно: всегда возвращается
// false. Иначе можно было бы случайно подобрать ключ, у которого
// поле kid тоже не заполнено, и пройти проверку, не имея в виду
// конкретный ключ. Защита простая, но избавляет от этого класса
// опечаток.
func (s Set) Find(kid string) (JWK, bool) {
	if kid == "" {
		return JWK{}, false
	}
	for _, k := range s.Keys {
		if k.Kid == kid {
			return k, true
		}
	}
	return JWK{}, false
}
