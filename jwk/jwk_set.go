package jwk

import "encoding/json"

// Set — набор JWK, как описано в RFC 7517 §5 (JWKSet).
//
// Используется сервером авторизации, чтобы публиковать свои публичные ключи
// (эндпоинт /.well-known/pq-jwks): валидаторы скачивают набор и проверяют
// подписи токенов по тому ключу, чей kid указан в заголовке.
//
// Поле Keys именно так и называется — этого требует RFC 7517 §5.1.
type Set struct {
	Keys []JWK `json:"keys"`
}

// MarshalJSON гарантирует, что пустой набор сериализуется как `{"keys":[]}`,
// а не `{"keys":null}` — иначе JWKS-клиенты на других языках, где null-array
// и пустой массив не взаимозаменяемы, могут отказаться парсить набор.
func (s Set) MarshalJSON() ([]byte, error) {
	keys := s.Keys
	if keys == nil {
		keys = []JWK{}
	}
	type wire struct {
		Keys []JWK `json:"keys"`
	}
	return json.Marshal(wire{Keys: keys})
}

// Find возвращает ключ из набора по его kid. Если такого ключа нет — bool=false.
//
// Ключи перебираются по порядку, и возвращается первый совпавший. RFC 7517
// рекомендует, но не требует уникальности kid в наборе; если в JWKS оказались
// два ключа с одинаковым kid (например, во время ротации), выбор первого —
// разумный детерминированный вариант.
//
// Пустой kid считается «kid не указан» и всегда даёт false: иначе можно было
// бы случайно подобрать ключ, у которого поле kid тоже пустое, и пройти
// проверку, не имея в виду конкретный ключ.
func (s Set) Find(kid string) (JWK, bool) {
	if kid == "" {
		return JWK{}, false
	}
	for _, k := range s.Keys {
		if k.Kid == kid {
			return k, true
		}
	}
	return JWK{}, false
}
