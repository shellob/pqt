package authserver

import (
	"sync"
	"time"
)

// RevocationStore — чёрный список идентификаторов отозванных токенов
// (по claim jti). Если jti токена попал сюда, валидатор обязан считать
// этот токен недействительным, даже если по подписи и сроку всё
// в порядке.
//
// Когда это нужно. Самый распространённый сценарий — пользователь
// нажал «выйти из системы». Сам access-токен у него на руках всё ещё
// валиден (срок не истёк), но сервер должен его отвергать. Запись
// в чёрный список — самый прямой способ это сделать. Аналогично — при
// принудительном выходе всех сессий пользователя при смене пароля.
//
// Метод IsRevoked имеет ту же сигнатуру, что и поле IsRevoked в
// pqt.ValidateOptions. Это позволяет передавать его напрямую — сервер
// ресурсов в своём ValidateOptions ставит `IsRevoked: store.IsRevoked`
// и Validate сам обращается к чёрному списку при проверке каждого
// токена.
//
// # Ограничения прототипа
//
// Хранилище работает в памяти процесса: при перезапуске сервера
// чёрный список обнуляется. Для прототипа это нормально, потому что
// access-токены живут всего 15 минут — даже без чёрного списка они
// сами перестанут работать после истечения срока. В production-сервисе
// здесь нужно постоянное хранилище (Redis или база), и ещё общая
// видимость между экземплярами сервера, если их несколько.
type RevocationStore struct {
	mu      sync.RWMutex
	revoked map[string]time.Time // jti → время отзыва
}

// NewRevocationStore создаёт пустой чёрный список.
func NewRevocationStore() *RevocationStore {
	return &RevocationStore{revoked: make(map[string]time.Time)}
}

// Revoke добавляет jti в чёрный список с пометкой времени отзыва.
// Время потом нигде не используется (мы не делаем автоматическую
// очистку записей по сроку), но хранится на случай отладки —
// видно, когда токен был отозван.
//
// Повторный вызов с тем же jti перезаписывает время, это безопасно.
// Пустой jti игнорируется: токены без jti отозвать невозможно
// (по их идентификатору их в списке потом не найти).
func (s *RevocationStore) Revoke(jti string, at time.Time) {
	if jti == "" {
		return
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	s.revoked[jti] = at
}

// IsRevoked отвечает на вопрос «этот jti в чёрном списке?».
// Возвращает true только для тех jti, которые были явно положены
// в Revoke; пустой jti всегда даёт false (см. Revoke).
//
// Сигнатура специально подобрана под pqt.ValidateOptions.IsRevoked —
// серверу ресурсов достаточно сделать `opts.IsRevoked = store.IsRevoked`,
// и каждый Validate сам проверит токен в чёрном списке.
func (s *RevocationStore) IsRevoked(jti string) bool {
	if jti == "" {
		return false
	}
	s.mu.RLock()
	defer s.mu.RUnlock()
	_, ok := s.revoked[jti]
	return ok
}
