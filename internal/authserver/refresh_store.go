package authserver

import (
	"sync"
	"time"
)

// RefreshSession — запись о выпущенном refresh-токене на стороне сервера.
//
// Что такое refresh-токен. У OAuth-токенов классически два срока: access
// живёт коротко (минуты), а refresh — долго (дни/недели). Когда access
// истекает, клиент идёт на /auth/refresh со своим refresh-токеном и
// получает новую пару. Так пользователю не нужно каждые 15 минут вводить
// пароль заново.
//
// Зачем хранить запись на сервере, если refresh — это самостоятельный
// PQ-AT-токен с подписью. Чтобы:
//
//   - убедиться, что присланный токен действительно был выпущен этим
//     сервером (если jti отсутствует в записях, токен либо не наш,
//     либо уже отозван);
//   - привязать обновление к конкретному пользователю и его scope —
//     это нам не нужно вычитывать из самого токена, проще хранить;
//   - заметить повторное использование уже использованного refresh
//     (флаг Used). Это типичный признак компрометации, описанный
//     в RFC 6749 §10.4 в разделе про ротацию refresh-токенов: если
//     один и тот же refresh пытаются разменять дважды, значит
//     либо у легального клиента случился сетевой сбой и он повторил
//     запрос, либо токен украли. Мы безопасно реагируем «отказать»
//     в обоих случаях.
type RefreshSession struct {
	JTI       string
	Username  string
	Scope     string
	IssuedAt  time.Time
	ExpiresAt time.Time

	// Used становится true после первой успешной попытки разменять
	// этот refresh на новую пару токенов. В прототипе мы реагируем
	// минимально: отказываем при второй попытке и пишем warn в лог.
	// В production-сервере здесь обычно делают каскадную
	// инвалидацию — отзывают всю цепочку токенов, выпущенных по
	// этому refresh, на случай что украли именно его. Этого мы
	// сейчас не реализовываем.
	Used bool
}

// RefreshStore — in-memory хранилище refresh-сессий, ключ — jti токена.
// «In-memory» значит, что при перезапуске сервера все записи
// теряются: для прототипа диссертации это допустимо, а в production
// сервисах хранилище обычно делается на Redis или базе данных.
//
// Параллельный доступ защищён обычным sync.RWMutex: чтение в Get/Has
// идёт под RLock (могут идти параллельно), запись в Save/MarkUsed/Delete —
// под Lock (не могут идти параллельно ни друг с другом, ни с чтениями).
type RefreshStore struct {
	mu       sync.RWMutex
	sessions map[string]*RefreshSession
}

// NewRefreshStore создаёт пустое хранилище.
func NewRefreshStore() *RefreshStore {
	return &RefreshStore{sessions: make(map[string]*RefreshSession)}
}

// Save кладёт новую сессию в хранилище. Принимает значение, а не
// указатель, и копирует его внутрь: так вызывающий код, держа у себя
// ту же структуру, не сможет случайно поменять данные хранилища
// постфактум.
//
// Перезапись по существующему jti не предполагается — jti уникален
// по построению (16 случайных байт). Но если кто-то всё-таки повторит
// jti, последняя запись просто заменит предыдущую.
func (s *RefreshStore) Save(sess RefreshSession) {
	s.mu.Lock()
	defer s.mu.Unlock()
	cp := sess
	s.sessions[sess.JTI] = &cp
}

// Get возвращает сессию по jti. Если jti не найден — bool=false.
//
// Возвращается копия структуры, не указатель на внутреннюю запись.
// Это снова защита от случайных мутаций: если бы мы возвращали
// указатель, вызывающий код мог бы поменять, скажем, ExpiresAt — и
// внутреннее состояние хранилища изменилось бы помимо MarkUsed/Delete.
func (s *RefreshStore) Get(jti string) (*RefreshSession, bool) {
	s.mu.RLock()
	defer s.mu.RUnlock()
	sess, ok := s.sessions[jti]
	if !ok {
		return nil, false
	}
	cp := *sess
	return &cp, true
}

// MarkUsed помечает сессию как использованную (флаг Used=true).
// Возвращает true, если пометка действительно поставилась первый раз,
// и false в двух случаях: сессии нет, или она уже была помечена раньше.
//
// Это атомарная операция «проверь и пометь под одним замком» — она и
// есть защита от гонок при ротации refresh-токена. Если бы мы делали
// «сначала Get, потом Save с Used=true», между двумя вызовами могла
// бы вклиниться вторая горутина, тоже прочитать Used=false и тоже
// принять решение «можно ротировать» — и refresh успешно разменялся
// бы дважды.
func (s *RefreshStore) MarkUsed(jti string) bool {
	s.mu.Lock()
	defer s.mu.Unlock()
	sess, ok := s.sessions[jti]
	if !ok || sess.Used {
		return false
	}
	sess.Used = true
	return true
}

// Delete удаляет сессию из хранилища. Используется при отзыве
// refresh-токена через /auth/revoke. Если такой сессии нет, ничего
// не происходит: по RFC 7009 §2.2 эндпоинт revoke обязан отвечать
// успехом в любом случае, не сообщая клиенту, был такой токен или
// нет.
func (s *RefreshStore) Delete(jti string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	delete(s.sessions, jti)
}
