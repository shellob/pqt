package authserver

import (
	"fmt"

	"golang.org/x/crypto/bcrypt"
)

// seedUserSpec — описание одного зашитого в код пользователя: пароль в
// открытом виде и набор разрешённых scope. На старте сервера пароль
// прогоняется через bcrypt, и в памяти остаётся только полученный хеш —
// открытый пароль не сохраняется.
//
// Это прототип диссертации, а не настоящий сервер. В реальном
// развёртывании пользователи и хеши паролей лежали бы в базе данных,
// добавлялись бы через регистрацию, и т. д. Для воспроизводимости
// эксперимента из глав 4.2–4.6 удобнее иметь маленький фиксированный
// набор учёток, известный заранее, чтобы тесты и нагрузочные прогоны
// всегда работали с одними и теми же данными.
type seedUserSpec struct {
	Password string
	Scope    string
}

// seedUsers — четыре зашитых пользователя с разными уровнями доступа.
// Используются в тестах, демо-странице и нагрузочных сценариях.
//
// Scope — это строка с правами через пробел, в стиле OAuth (RFC 6749). У alice есть «read write», у bob только «read», у charlie
// дополнительно «admin», у dave снова базовое «read». На сервере
// ресурсов middleware RequireScope проверит наличие нужного значения
// в этой строке.
var seedUsers = map[string]seedUserSpec{
	"alice":   {Password: "alice-password-2026", Scope: "read write"},
	"bob":     {Password: "bob-password-2026", Scope: "read"},
	"charlie": {Password: "charlie-password-2026", Scope: "read write admin"},
	"dave":    {Password: "dave-password-2026", Scope: "read"},
}

// User — пользователь в памяти сервера. Хранит хеш пароля (PasswordHash),
// а не сам пароль. Это значит: даже если кто-то получит дамп процесса,
// исходные пароли он оттуда не достанет — только хеши, по которым
// нужно подбирать перебором.
type User struct {
	Username     string
	PasswordHash []byte
	Scope        string
}

// UserStore — хранилище seed-пользователей. Создаётся один раз при
// старте сервера и дальше только читается (Authenticate). Никаких
// мьютексов внутри нет, потому что записи после инициализации не
// бывает.
type UserStore struct {
	users     map[string]User
	dummyHash []byte
}

// NewUserStore создаёт хранилище и прогоняет пароли всех seed-пользователей
// через bcrypt. Параметр bcryptCost задаёт сложность хеширования.
//
// Что такое bcryptCost. bcrypt — намеренно медленная функция; параметр
// cost (целое от 4 до 31) определяет, насколько медленная: каждое
// увеличение на 1 удваивает время. cost=10 — то, что обычно используется
// в production, занимает примерно 60 мс на современном CPU. cost=4 —
// минимум, около 1 мс; используется в тестах, чтобы старт каждого
// тестового сервера не занимал секунды на хеширование четырёх
// seed-юзеров.
//
// # Зачем мы дополнительно генерируем dummy-хеш
//
// Вторым шагом мы хешируем строку «dummy» с тем же cost. Получившийся
// хеш не используется ни одним пользователем — это «заглушка». Зачем
// она нужна, описано в комментарии к Authenticate.
//
// Главное здесь: dummy-хеш сгенерирован с тем же cost, что и реальные.
// Если бы он был с фиксированным cost (например, всегда 4) или статически
// зашит в код, то при production-cost=10 проверка против него заняла
// бы 1 мс, а проверка против реального пользователя — 60 мс. Атакующий
// просто замерял бы время отклика и понимал, какой логин существует.
func NewUserStore(bcryptCost int) (*UserStore, error) {
	users := make(map[string]User, len(seedUsers))
	for name, spec := range seedUsers {
		hash, err := bcrypt.GenerateFromPassword([]byte(spec.Password), bcryptCost)
		if err != nil {
			return nil, fmt.Errorf("authserver: bcrypt-хеш пользователя %q: %w", name, err)
		}
		users[name] = User{
			Username:     name,
			PasswordHash: hash,
			Scope:        spec.Scope,
		}
	}

	dummy, err := bcrypt.GenerateFromPassword([]byte("dummy"), bcryptCost)
	if err != nil {
		return nil, fmt.Errorf("authserver: bcrypt-хеш заглушки: %w", err)
	}

	return &UserStore{users: users, dummyHash: dummy}, nil
}

// Authenticate проверяет логин и пароль. При совпадении возвращает
// пользователя и true; при любой неудаче — пустую структуру и false.
//
// Сервер сознательно не различает в ответе случаи «такого пользователя
// нет» и «пароль не подходит»: иначе злоумышленник по разнице ответов
// мог бы сначала перебором найти существующие логины, а уже потом
// подбирать к ним пароли. Из такой атаки делается одна — подбор пары
// логин-пароль сразу.
//
// Тут есть тонкость, которая на первый взгляд кажется лишним кодом.
// Ниже мы прогоняем bcrypt-сравнение даже когда пользователя в
// хранилище нет — против заранее заготовленного заглушечного хеша
// (s.dummyHash), результат отбрасываем. Это сделано не зря.
//
// Bcrypt — намеренно медленная функция, проверка хеша занимает
// десятки миллисекунд (точное время задано параметром bcryptCost — у
// нас по умолчанию 10, около 60 мс на современном CPU). Если бы при
// отсутствии логина мы сразу возвращали false, сервер отвечал бы за
// микросекунды, а реальным пользователям — после 60-миллисекундной
// задержки на bcrypt. Эту разницу во времени можно было бы измерять
// снаружи и определять, какие логины в системе существуют. Холостое
// сравнение выравнивает время ответа.
func (s *UserStore) Authenticate(username, password string) (User, bool) {
	u, ok := s.users[username]
	if !ok {
		_ = bcrypt.CompareHashAndPassword(s.dummyHash, []byte(password))
		return User{}, false
	}
	if err := bcrypt.CompareHashAndPassword(u.PasswordHash, []byte(password)); err != nil {
		return User{}, false
	}
	return u, true
}
