package pqt

import (
	"time"

	"pqt/keys"
	"pqt/token"
)

// Clock — функция, возвращающая текущее время. Validate использует её,
// когда нужно сравнить с claim exp (срок действия токена).
//
// В обычной работе сюда подставляется time.Now: реальное системное время.
// В тестах удобно подменить функцию на фиксированный момент — тогда
// можно точно воспроизвести поведение на границе exp без зависимости
// от того, когда тест запустили.
type Clock func() time.Time

// KeySource — функция, которая по заголовку токена возвращает публичный
// ключ для проверки подписи. Без такого ключа Validate не сможет
// удостовериться, что токен действительно подписан тем, кому мы доверяем.
//
// Реализация — на усмотрение того, кто пользуется библиотекой. Возможные
// варианты: всегда возвращать один и тот же ключ (для теста или совсем
// простой конфигурации); искать ключ в jwk.Set по полю Kid из заголовка
// (стандартный путь, когда у сервера несколько ключей и есть JWKS-эндпоинт);
// сходить за ключом во внешний сервис. Если по какой-то причине ключ
// не нашёлся, нужно вернуть ErrKeyNotFound (или ошибку, обёрнутую этим
// тегом — Validate всё равно поймает её через errors.Is).
//
// # Важное правило безопасности
//
// Ни в коем случае не выбирай ключ по header.Alg. Это поле приходит
// извне вместе с токеном, и его значение контролирует тот, кто токен
// прислал — то есть потенциально и злоумышленник. Если KeySource
// будет выбирать ключ по alg, атака получается такая: подделыватель
// подменяет alg на «слабый» алгоритм, KeySource послушно отдаёт ключ
// под этот «слабый» алгоритм, а Validate потом сверит alg-в-токене с
// alg-у-ключа — и они совпадут, потому что подмена случилась в обоих
// местах одинаково. Защита, которую делает Validate, обнулится.
//
// Правильный источник для выбора ключа — header.Kid, идентификатор
// конкретного ключа. Его эмитент проставляет сам и потом публикует
// в JWKS под тем же значением. Альтернатива — заранее зашитый
// единственный ключ, без выбора вообще.
//
// Безопасный пример:
//
//	func keyByKid(set jwk.Set) pqt.KeySource {
//	    return func(h token.Header) (keys.PublicKey, error) {
//	        j, ok := set.Find(h.Kid)
//	        if !ok {
//	            return nil, pqt.ErrKeyNotFound
//	        }
//	        return jwk.ParsePublic(j)
//	    }
//	}
type KeySource func(header token.Header) (keys.PublicKey, error)

// IssueOptions — настройки выпуска токена через Issue.
//
// Тут осознанно нет поля Alg. Алгоритм подписи всегда берётся из
// Signer.Algorithm(), и именно он попадает в заголовок токена в поле
// alg. Если бы Alg указывался отдельно в опциях, можно было бы
// случайно (или специально) собрать токен, у которого в заголовке
// один алгоритм, а реально подписан он другим — это сбило бы
// Validate с толку и открыло путь к атаке. С единым источником такой
// расклад в принципе невозможен.
type IssueOptions struct {
	// Signer — приватный ключ, которым подписывается склейка
	// заголовка и payload (H || P). Тип PrivateKey — это интерфейс
	// из пакета keys; конкретная реализация определяет алгоритм
	// (ECDSA, ML-DSA или гибрид).
	Signer keys.PrivateKey

	// Codec — формат, в котором сериализуются claims (полезная
	// нагрузка): JSON со строковыми именами полей или CBOR с
	// целочисленными ключами в стиле CWT. Записывается в заголовок
	// в поле enc, чтобы получатель токена знал, как payload разбирать.
	Codec token.Codec

	// Format — как итоговый токен будет упакован: текстовый вид
	// (три части через точку, в base64url) или бинарный (длины и
	// блоки подряд, без base64). На само содержимое заголовка и
	// payload не влияет, только на способ их склейки.
	Format token.Format

	// Kid — идентификатор ключа (key id). Если у эмитента в обороте
	// несколько ключей одновременно (например, во время плановой
	// смены ключей), Kid говорит получателю, каким именно ключом
	// токен подписан. Тогда KeySource на стороне проверки сможет
	// выбрать в JWKS правильный ключ. Если ключ один — поле можно
	// не заполнять.
	Kid string
}

// ValidateOptions — настройки проверки токена через Validate.
type ValidateOptions struct {
	// KeySource — функция, которая по заголовку токена возвращает
	// публичный ключ для проверки подписи. Обязательное поле:
	// без ключа сверять подпись не с чем. Подробнее о безопасном
	// написании этой функции — см. документацию типа KeySource выше.
	KeySource KeySource

	// Format — в каком виде токен пришёл: text или binary. Обязательное
	// поле, потому что библиотека не пытается угадывать формат по
	// содержимому. Вызывающий код всегда знает формат из контекста:
	// в HTTP-заголовке Authorization: Bearer всегда text, в gRPC-метадате
	// и других бинарных каналах — binary.
	Format token.Format

	// ExpectedIssuer — какое значение должно лежать в claim iss
	// (издатель токена). Если задано, Validate сравнит и при несовпадении
	// вернёт ErrIssuerMismatch. Если оставить пустую строку — проверка
	// пропускается, любой iss будет принят. Пустую строку имеет смысл
	// оставлять в первоначальной отладке; в production это всегда должен
	// быть конкретный URL вашего сервера авторизации.
	ExpectedIssuer string

	// ExpectedAudience — для какой системы предназначен токен (claim
	// aud). Если задано — Validate сверит и при несовпадении вернёт
	// ErrAudienceMismatch. Пустая строка — проверка пропускается.
	// Это защита от сценария, где злоумышленник получает токен,
	// выписанный для одной системы, и предъявляет его другой.
	ExpectedAudience string

	// Clock — источник текущего времени для сравнения с claim exp
	// (срок действия). Если оставить nil, Validate возьмёт time.Now.
	// В тестах сюда передают функцию, возвращающую фиксированный
	// момент — так можно проверить ровно ту границу exp, которую
	// нужно, и не зависеть от времени запуска теста.
	Clock Clock

	// Leeway — на сколько позволено расходиться часам того, кто
	// выпустил токен, и того, кто его проверяет. Если на сервере-эмитенте
	// время на 10 секунд впереди, токен с очень коротким временем жизни
	// мог бы прийти к проверке как «уже просроченный», хотя по
	// эмитенту он ещё валиден. Leeway такого размера эту разницу
	// проглотит. По умолчанию ноль, никаких поблажек: типичный
	// безопасный диапазон — 30-60 секунд при NTP-синхронизации.
	Leeway time.Duration

	// IsRevoked — опциональная функция, которая отвечает на вопрос:
	// «токен с таким jti отозван?». Validate вызывает её после
	// успешной проверки подписи и срока действия; если функция
	// вернула true, Validate отдаст ErrTokenRevoked. jti — это claim
	// «JWT ID», уникальный идентификатор конкретного токена.
	//
	// Реализацию выбирает тот, кто использует библиотеку: чёрный
	// список в памяти, запрос к серверу авторизации, общий кеш на
	// несколько процессов — что подходит под задачу. Если поле
	// оставить nil, проверка отзыва не делается.
	IsRevoked func(jti string) bool
}
